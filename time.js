$(document).ready(function(){
    $(".show").click(function(){
    $(".links").fadeIn(1000 );
    $(".show").fadeOut(500 );
    $(".hide").fadeIn(500 );
    });
    });


$(document).ready(function(){
    $(".hide").click(function(){
    $(".links").fadeOut(1000 );
    $(".show").fadeIn(500 );
    $(".hide").fadeOut(500 );
    });
    });
    